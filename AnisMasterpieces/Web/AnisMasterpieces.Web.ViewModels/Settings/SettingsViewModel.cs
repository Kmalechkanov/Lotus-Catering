namespace AnisMasterpieces.Web.ViewModels.Settings
{
    using AnisMasterpieces.Data.Models;
    using AnisMasterpieces.Services.Mapping;
    using AutoMapper;

    public class SettingsViewModel : IMapFrom<Setting>, IHaveCustomMappings
    {
        public int Id { get; set; }

        public string Name { get; set; }

        public string Value { get; set; }

        public string NameAndValue { get; set; }

        public void CreateMappings(IProfileExpression configuration)
        {
            configuration.CreateMap<Setting, SettingsViewModel>().ForMember(
                m => m.NameAndValue,
                opt => opt.MapFrom(x => x.Name + " = " + x.Value));
        }
    }
}
